import type { CoinChartBootstrapProps } from '../interface/CoinChart.interface';
import { useCoinChartBootstrapChart } from './CoinChartBootstrapChart.logic';
import { useCoinChartBootstrapSync } from './CoinChartBootstrapSync.logic';

export function useCoinChartBootstrap({
  candles,
  containerRef,
  interval,
  isChartEnabled,
  priceScaleOverlayRef,
  wrapperRef,
  isLoadingMore,
  hasMoreOlderCandles,
  onLoadOlderCandles,
  chartRef,
  seriesRef,
  ma10SeriesRef,
  ma50SeriesRef,
  ma100SeriesRef,
  ma200SeriesRef,
  pivotHighSeriesRef,
  pivotLowSeriesRef,
  structureMarkersRef,
  setHoveredCandle,
  setIsChartReady,
  candlesRef,
  onLoadOlderCandlesRef,
  hasMoreOlderCandlesRef,
  isLoadingMoreRef,
  requestedBeforeOpenTimeRef,
  pendingRangeRef,
  isProgrammaticRangeChangeRef,
  timeScaleWheelDeltaRef,
  timeScaleZoomRef,
  shouldFollowLatestRef,
}: CoinChartBootstrapProps) {
  useCoinChartBootstrapSync({
    candles,
    candlesRef,
    hasMoreOlderCandles,
    hasMoreOlderCandlesRef,
    isLoadingMore,
    isLoadingMoreRef,
    onLoadOlderCandles,
    onLoadOlderCandlesRef,
    priceScaleOverlayRef,
    timeScaleWheelDeltaRef,
    timeScaleZoomRef,
    wrapperRef,
    chartRef,
    seriesRef,
  });

  useCoinChartBootstrapChart({
    candlesRef,
    chartRef,
    containerRef,
    interval,
    isChartEnabled,
    hasMoreOlderCandlesRef,
    isLoadingMoreRef,
    isProgrammaticRangeChangeRef,
    ma100SeriesRef,
    ma10SeriesRef,
    ma200SeriesRef,
    ma50SeriesRef,
    onLoadOlderCandlesRef,
    pendingRangeRef,
    pivotHighSeriesRef,
    pivotLowSeriesRef,
    requestedBeforeOpenTimeRef,
    seriesRef,
    setHoveredCandle,
    setIsChartReady,
    shouldFollowLatestRef,
    structureMarkersRef,
  });
}
